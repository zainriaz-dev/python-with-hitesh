# Integer examples

# Example with Addition
kashmiri_tea_grams = 15
cinnamon_grams = 5
total_grams = kashmiri_tea_grams + cinnamon_grams
print(f"Total grams of base tea is {total_grams}")

# Example with Subtraction
remaining_tea = kashmiri_tea_grams - cinnamon_grams
print(f"Total remaining grams of tea is {remaining_tea}")

# Example with Division
milk_litres = 7
servings = 3
milk_per_serving = milk_litres / servings
print(f"Milk per serving is {milk_per_serving}")

# Example with Floor Division
total_cinnamon_bags = 15
total_pods = 5
bags_per_pod = total_cinnamon_bags // total_pods
print(f"Bags per pod is {bags_per_pod}")

# Example with Modulus
total_cardamom_bags = 10
pods_per_cup = 3
remaining_pods = total_cardamom_bags % pods_per_cup
print(f"Total remaining pods: {remaining_pods}")

# Example with Exponent
base_flavor_strength = 2
scale_factor = 3
demanded_flavor = base_flavor_strength ** scale_factor
print(f"Demanded Flavor Strength: {demanded_flavor}")

# Big Numbers with Underscores (Improve Readability)
total_tea_leaves_harvested = 1_000_000_000
print(f"Total Tea leaves harvested {total_tea_leaves_harvested}")
